/* ENCODER */
void encoder_update_user(uint8_t index, bool clockwise) {
     if (index == 0) {
            if (clockwise) {
            tap_code16(C(KC_PLUS));		//ZOOM IN (IN BROWSERS)
            } 
			else {
            tap_code16(C(KC_MINUS));	//ZOOM OUT (IN BROWSERS)
			}
	}
    else if (index == 1) {
            if (clockwise) {
            tap_code(KC_VOLU);
            } 
			else {
            tap_code(KC_VOLD);
			}
	}
	else if (index == 2) {
            if (clockwise) {
            tap_code(KC_PGDOWN);
            } 
			else {
            tap_code(KC_PGUP);
			}	
	}
}
